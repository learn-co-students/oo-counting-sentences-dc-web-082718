require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      return true
    else
      return false
    end
  end

  def question?
    if self.end_with?("?")
      return true
    else
      return false
    end
  end

  def exclamation?
    if self.end_with?("!")
      return true
    else
      return false
    end
  end

  def count_sentences
    sentence = self.split (/[.?!]/)
    no_empty_sentences = sentence.reject { |x| x.empty? }
    no_empty_sentences.count
  end
end
